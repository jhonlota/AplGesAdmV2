    public void metodoInsertar() {
        ClaseGeneral.serviciossolicitudesoferta = new Serviciossolicitudesoferta();
        ClaseGeneral.serviciossolicitudesofertaPK = new ServiciossolicitudesofertaPK();
        
        ClaseGeneral.serviciossolicitudesofertaPK.setFksolicitudoferta("" + fksolicitudoferta.getText());
        ClaseGeneral.serviciossolicitudesofertaPK.setId(0);
        ClaseGeneral.serviciossolicitudesofertaPK.setAno(ClaseGeneral.solicitudesoferta.getAno());
        
        ClaseGeneral.serviciossolicitudesoferta.setServiciossolicitudesofertaPK(ClaseGeneral.serviciossolicitudesofertaPK);
        ClaseGeneral.serviciossolicitudesoferta.setNombre(nombre.getText());
        ClaseGeneral.serviciossolicitudesoferta.setDescripcion(descripcion.getText());
        ClaseGeneral.serviciossolicitudesoferta.setCaracteristicas(caracteristicas.getText());
        ClaseGeneral.serviciossolicitudesoferta.setCantidad(Integer.parseInt(""+cantidad.getText()));  

        if (ClaseGeneral.controlServiciossolicitudesoferta.verify(ClaseGeneral.serviciossolicitudesoferta)) {
            try {
                ClaseGeneral.controlServiciossolicitudesoferta.create(ClaseGeneral.serviciossolicitudesoferta);
            } catch (Exception ex) {
                Logger.getLogger(PanelServiciossolicitudesoferta.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

    public void metodoConsultar() {
        try {
            fksolicitudoferta.setText("" + ClaseGeneral.serviciossolicitudesoferta.getServiciossolicitudesofertaPK().getFksolicitudoferta());
            nombre.setText(ClaseGeneral.serviciossolicitudesoferta.getNombre());
            descripcion.setText(ClaseGeneral.serviciossolicitudesoferta.getDescripcion());
            caracteristicas.setText(ClaseGeneral.serviciossolicitudesoferta.getCaracteristicas());
            cantidad.setText(""+ClaseGeneral.serviciossolicitudesoferta.getCantidad());
            
        } catch (Exception ex) {
            Logger.getLogger(PanelServiciossolicitudesoferta.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void metodoActualizar() {
        
        Serviciossolicitudesoferta serviciossolicitudesofertaId = ClaseGeneral.serviciossolicitudesoferta;
        serviciossolicitudesofertaId.setServiciossolicitudesofertaPK(ClaseGeneral.serviciossolicitudesoferta.getServiciossolicitudesofertaPK());

        ClaseGeneral.serviciossolicitudesoferta = new Serviciossolicitudesoferta();
        ClaseGeneral.serviciossolicitudesofertaPK = new ServiciossolicitudesofertaPK();
        ClaseGeneral.serviciossolicitudesofertaPK.setFksolicitudoferta(fksolicitudoferta.getText());
        //ClaseGeneral.soportescomprobantesPK.setId();
        ClaseGeneral.serviciossolicitudesofertaPK.setAno(ClaseGeneral.solicitudesoferta.getAno());
        ClaseGeneral.serviciossolicitudesoferta.setServiciossolicitudesofertaPK(ClaseGeneral.serviciossolicitudesofertaPK);
        ClaseGeneral.serviciossolicitudesoferta.setNombre(nombre.getText());
        ClaseGeneral.serviciossolicitudesoferta.setDescripcion(descripcion.getText());
        ClaseGeneral.serviciossolicitudesoferta.setCantidad(Integer.parseInt(cantidad.getText()));
        ClaseGeneral.serviciossolicitudesoferta.setCaracteristicas(caracteristicas.getText());

        if (ClaseGeneral.controlServiciossolicitudesoferta.verify(ClaseGeneral.serviciossolicitudesoferta)) {
            try {
                ClaseGeneral.controlServiciossolicitudesoferta.edit(ClaseGeneral.serviciossolicitudesoferta, serviciossolicitudesofertaId);
            } catch (Exception ex) {
                Logger.getLogger(PanelServiciossolicitudesoferta.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

    public void metodoEliminar() {
        try {
            
            ClaseGeneral.serviciossolicitudesofertaPK = new ServiciossolicitudesofertaPK();
            ClaseGeneral.serviciossolicitudesofertaPK.setFksolicitudoferta(fksolicitudoferta.getText());
            ClaseGeneral.serviciossolicitudesofertaPK.setId(ClaseGeneral.serviciossolicitudesoferta.getServiciossolicitudesofertaPK().getId());
            ClaseGeneral.serviciossolicitudesofertaPK.setAno(ClaseGeneral.solicitudesoferta.getAno());

            ClaseGeneral.controlServiciossolicitudesoferta.destroy(ClaseGeneral.serviciossolicitudesofertaPK);
        } catch (Exception ex) {
            Logger.getLogger(PanelServiciossolicitudesoferta.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void metodoMostrar() {
        BigDecimal valTotal = BigDecimal.ZERO;
        ClaseGeneral.serviciossolicitudesoferta = new Serviciossolicitudesoferta();
        lista = ClaseGeneral.controlServiciossolicitudesoferta.findAllInServiciossolicitudesofertaByFksolicitudofertaAno(fksolicitudoferta.getText(), ClaseGeneral.solicitudesoferta.getAno());

        jTable.getColumnModel().getColumn(3).setCellRenderer(informacion.modeloDerecha);

        for (int i = 0; i < 100; i++) {
            jTable.setValueAt("", i, 0);
            jTable.setValueAt("", i, 1);
            jTable.setValueAt("", i, 2);
            jTable.setValueAt("", i, 3);
        }

        int i = 0;
        for (Object lista1 : lista) {
            ClaseGeneral.serviciossolicitudesoferta = (Serviciossolicitudesoferta) lista1;
            jTable.setValueAt("" + ClaseGeneral.serviciossolicitudesoferta.getNombre(), i, 0);
            jTable.setValueAt("" + ClaseGeneral.serviciossolicitudesoferta.getDescripcion(), i, 1);
            jTable.setValueAt("" + ClaseGeneral.serviciossolicitudesoferta.getCaracteristicas(), i, 2);
            jTable.setValueAt("" + ClaseGeneral.serviciossolicitudesoferta.getCantidad(), i, 3);
            i++;
        }

        jTable.clearSelection();
        jTable.getColumnModel().getColumn(0).setPreferredWidth(110);
        jTable.getColumnModel().getColumn(1).setPreferredWidth(110);
        jTable.getColumnModel().getColumn(2).setPreferredWidth(210);
        jTable.getColumnModel().getColumn(3).setPreferredWidth(10);
    }
