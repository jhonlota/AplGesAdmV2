        ClaseGeneral.cuentas.setFktercero("" + fktercero.getText());
        ClaseGeneral.cuentas.setIngreso(BigDecimal.valueOf(Long.parseLong("" + ingreso.getText().trim().replace(".", ""))));
        ClaseGeneral.cuentas.setSoporte(soporte.getText());
        ClaseGeneral.cuentas.setFkcontrato(fkcontrato.getText());
        ClaseGeneral.cuentas.setNumeroseguimiento(numeroseguimiento.getText());

