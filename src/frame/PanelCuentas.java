    public void metodoMostrarSoportesCuentas() {
        BigDecimal valTotal = BigDecimal.ZERO;
        ClaseGeneral.soportescuentas = new Soportescuentas();
        lista = ClaseGeneral.controlSoportescuentas.findAllInSoportescuentasByFkcuenta(ClaseGeneral.cuentas.getSoporte());

        jTableSoportesCuentas.getColumnModel().getColumn(7).setCellRenderer(informacion.modeloDerecha);

        for (int i = 0; i < 100; i++) {
            jTableSoportesCuentas.setValueAt("", i, 0);
            jTableSoportesCuentas.setValueAt("", i, 1);
            jTableSoportesCuentas.setValueAt("", i, 2);
            jTableSoportesCuentas.setValueAt("", i, 3);
            jTableSoportesCuentas.setValueAt("", i, 4);
            jTableSoportesCuentas.setValueAt("", i, 5);
            jTableSoportesCuentas.setValueAt("", i, 6);
            jTableSoportesCuentas.setValueAt("", i, 7);
        }

        int i = 0;
        for (Object lista1 : lista) {
            ClaseGeneral.soportescuentas = (Soportescuentas) lista1;
            jTableSoportesCuentas.setValueAt("" + ClaseGeneral.soportescuentas.getCuenta(), i, 0);
            jTableSoportesCuentas.setValueAt("" + ClaseGeneral.soportescuentas.getActividad(), i, 1);
            jTableSoportesCuentas.setValueAt("" + ClaseGeneral.soportescuentas.getSubgrupo(), i, 2);
            jTableSoportesCuentas.setValueAt("" + ClaseGeneral.soportescuentas.getCingreso(), i, 3);
            jTableSoportesCuentas.setValueAt("" + ClaseGeneral.soportescuentas.getCcostos(), i, 4);
            jTableSoportesCuentas.setValueAt("" + ClaseGeneral.soportescuentas.getCinfo(), i, 5);
            jTableSoportesCuentas.setValueAt("" + ClaseGeneral.soportescuentas.getCuentainterna(), i, 6);
            jTableSoportesCuentas.setValueAt("" + ClaseInformacion.formatoDecimal.format(ClaseGeneral.soportescuentas.getValor()), i, 7);
            valTotal = valTotal.add(ClaseGeneral.soportescuentas.getValor());
            i++;
        }
        
        jTableSoportesCuentas.clearSelection();
        jTableSoportesCuentas.getColumnModel().getColumn(0).setPreferredWidth(90);
        jTableSoportesCuentas.getColumnModel().getColumn(1).setPreferredWidth(90);
        jTableSoportesCuentas.getColumnModel().getColumn(2).setPreferredWidth(90);
        jTableSoportesCuentas.getColumnModel().getColumn(3).setPreferredWidth(90);
        jTableSoportesCuentas.getColumnModel().getColumn(4).setPreferredWidth(90);
        jTableSoportesCuentas.getColumnModel().getColumn(5).setPreferredWidth(90);
        jTableSoportesCuentas.getColumnModel().getColumn(6).setPreferredWidth(125);
        jTableSoportesCuentas.getColumnModel().getColumn(7).setPreferredWidth(145);
        valorTotal.setText("" + ClaseInformacion.formatoDecimal.format(valTotal));
    }
    
