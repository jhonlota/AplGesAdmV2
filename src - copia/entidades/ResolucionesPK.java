package entidades;

public class ResolucionesPK {

    private int id;
    private int ano;

    public ResolucionesPK() {
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setAno(int ano) {
        this.ano = ano;
    }

    public int getId() {
        return id;
    }

    public int getAno() {
        return ano;
    }
}
