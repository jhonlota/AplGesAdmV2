    public void metodoInsertar() {
        if (accion.getSelectedItem().toString().equals("ENVIAR A DEPENDENCIA")) {
            observacion.setText("PENDIENTE\nENVIAR A DEPENDENCIA\n" + observacion.getText());
        } else if (accion.getSelectedItem().toString().equals("DEVOLVER A DEPENDENCIA")) {
            observacion.setText("PENDIENTE\nDEVOLVER A DEPENDENCIA\n" + observacion.getText());
        } else {
            observacion.setText("ENTREGA PRESUPUESTO\n" + observacion.getText());
        }

        ClaseGeneral.observacionescuentas = new Observacionescuentas();
        ClaseGeneral.observacionescuentasPK = new ObservacionescuentasPK();
        ClaseGeneral.observacionescuentasPK.setFkcuenta(fkcuenta.getText());
        ClaseGeneral.observacionescuentas.setObservacionescuentasPK(ClaseGeneral.observacionescuentasPK);
        ClaseGeneral.observacionescuentas.setDependencia("" + dependencia.getSelectedItem());
        ClaseGeneral.observacionescuentas.setObservacion(observacion.getText().toUpperCase());

        try {
            ClaseGeneral.controlObservacionescuentas.createTimestamp(ClaseGeneral.observacionescuentas);

            observacionescuentas = ClaseGeneral.controlObservacionescuentas.findAllInObservacionescuentasByFkcuentaMAXfecha(ClaseGeneral.cuentas.getSoporte());

            /**/
            ExternoPanelCalificacionTramites panelCalificacionTramites = new ExternoPanelCalificacionTramites();
            
            if (accion.getSelectedItem().toString().equals("ENVIAR A DEPENDENCIA")) {
            int opcion = ClaseMensaje.panel(panelCalificacionTramites);
            if (opcion == JOptionPane.YES_OPTION) {
                panelCalificacionTramites.metodoInsertar();
            }
            }
            
            /**/

            try {
                frameCarga.setVisible(true);
                frameCarga.pack();
                frameCarga.setSize(280, 100);
                frameCarga.setLocationRelativeTo(null);
                frameCarga.setResizable(false);
                hilo = new ThreadCarga();
                hilo.start();
            } catch (Exception e) {
            }
        } catch (Exception ex) {
            Logger.getLogger(PanelObservacionescuentas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    class ThreadCarga extends Thread {

        @Override
        public void run() {
            /**/
            boolean isERROR = false;
            frameCarga.jProgressBar.setIndeterminate(true);
            frameCarga.jProgressBar.setMinimum(1);
            frameCarga.jProgressBar.setMaximum(100);
            /**
             *
             */

            try {
                Map parametros = new HashMap();
                parametros.put("DEPENDENCIA", observacionescuentas.getDependencia());
                parametros.put("FECHA", ClaseInformacion.ConvertirFecha(observacionescuentas.getObservacionescuentasPK().getFecha()));
                parametros.put("OBSERVACION", (observacionescuentas.getObservacion().contains("DEVOLVER A DEPENDENCIA")
                        ? observacionescuentas.getObservacion().substring(32)
                        : observacionescuentas.getObservacion()));
                parametros.put("SOPORTE", observacionescuentas.getObservacionescuentasPK().getFkcuenta());
                parametros.put("MONTO", ClaseGeneral.cuentas.getEgreso());
                parametros.put("TIPO", ClaseGeneral.controlSoportescontratos.findTipoInSoportescontratosByFkcontratoRESGITRO(ClaseGeneral.cuentas.getFkcontrato()));
                parametros.put("FKCONTRATO", ClaseGeneral.cuentas.getFkcontrato());

                ClaseInformes informes = new ClaseInformes();
                if (ClaseGeneral.observacionescuentas.getObservacion().contains("ENVIAR A DEPENDENCIA")) {
                    if (ClaseInformacion.ValidarCALIFICACION(observacionescuentas.getObservacionescuentasPK().getFkcuenta())) {
                        informes.formatoCondicionCuentas(parametros);
                    } else {
                        isERROR = true;
                    }
                } else if (ClaseGeneral.observacionescuentas.getObservacion().contains("ENTREGA PRESUPUESTO")) {
                } else {
                    informes.formatoCondicion(parametros);
                }
            } catch (Exception e) {
                ClaseMensaje.error("Error al mostrar el archivo.\n" + e);
            }

            /**
             *
             */
            frameCarga.jProgressBar.setIndeterminate(false);
            frameCarga.dispose();
            hilo = null;

            if (isERROR) {
                ClaseMensaje.error("ERROR - La CUENTA NO ha sido Calificada");
            }
        }
    }
